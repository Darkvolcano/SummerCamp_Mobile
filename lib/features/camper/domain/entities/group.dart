  final int campId;
  final String campName;
  final int groupId;
  final String groupName;
  final int minAge;
  final int maxAge;

  const Group({
    required this.campId,
    required this.campName,
    required this.groupId,
    required this.groupName,
    required this.minAge,
    required this.maxAge,
  });
}
